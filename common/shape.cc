#include "shape.h"

#include "glad/glad.h"
#include "lo_common.h"
#include "material.h"
#include "shader.h"
#include "camera.h"

Shape::Shape() { model_ = glm::mat4(1.0); }

void Shape::Translate(const Vec3& v) { model_ = glm::translate(model_, v); }

void Shape::Scale(const Vec3& v) { model_ = glm::scale(model_, v); }

void Shape::Draw(GLContext* ctx) {
  unsigned int loc = material_->shader->GetUniformLocation("model");
  glUniformMatrix4fv(loc, 1, GL_FALSE, glm::value_ptr(model_));

  auto world = GetWorld();
  loc = material_->shader->GetUniformLocation("view");
  auto view = world->GetCamera()->GetViewMatrix();
  glUniformMatrix4fv(loc, 1, GL_FALSE, glm::value_ptr(view));

  loc = material_->shader->GetUniformLocation("projection");
  auto projection = world->GetCamera()->GetProjectionMatrix();
  glUniformMatrix4fv(loc, 1, GL_FALSE, glm::value_ptr(projection));

  glDrawArrays(GL_TRIANGLES, 0, vertex_size_);
}

